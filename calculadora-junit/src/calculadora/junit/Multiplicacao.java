package calculadora.junit;

/**
 * @author guima
 * @version 1.0
 * @since Release 1.0 da aplicação
 * 
 *  Esta classe representa a operação de multiplicação da calculadora.
 */
public class Multiplicacao {
	/**
	 * Método responsável por realizar a operação de multiplicação.
	 * 
	 * @param num1 Double - O valor do primeiro número.
	 * @param num2 Double - O valor do segundo número.
	 * @return Double - O resultado da multiplicação.
	 */
    public Double multiplicacao (Double num1, Double num2) {
        return num1 * num2;
    }
}

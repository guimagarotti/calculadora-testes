package calculadora.junit;

/**
 * @author guima
 * @version 1.0
 * @since Release 1.0 da aplicação
 * 
 *  Esta classe representa a operação de subtração da calculadora.
 */
public class Subtracao {
	/**
	 * Método responsável por realizar a operação de subtração.
	 * 
	 * @param num1 Double - O valor do primeiro número.
	 * @param num2 Double - O valor do segundo número.
	 * @return Double - O resultado da subtração.
	 */
    public Double subtracao(Double num1, Double num2) {
        return num1 - num2;
    }
}

package calculadora.junit;

/**
 * @author guima
 * @version 1.2
 * @since Release 1.2 da aplicação - 12/11/2023
 * 
 *  Esta classe representa a operação de soma da calculadora.
 */
public class Soma {
	/**
	 * Método responsável por realizar a operação de soma.
	 * 
	 * @param num1 Double - O valor do primeiro número.
	 * @param num2 Double - O valor do segundo número.
	 * @return Double - O resultado da soma.
	 */
    public Double soma(Double num1, Double num2) {
        return num1 + num2;
    }
}
